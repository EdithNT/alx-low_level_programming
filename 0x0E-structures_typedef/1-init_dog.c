#include "dog.h"
#include <stdio.h>

/**
 * init_dog -  initialize a variable of type struct dog
 * @d: pointer to dog
 * @name: dog's name
 * @age: dog's age
 * @owner: dog's owner
 *
 * Description: nothing
 */

void init_dog(struct dog *d, char *name, float age, char *owner)
{
	struct dog our_dog;

	if (our_dog == NULL)
		return (NULL);
	our_dog->d;
	our_dog->name;
	our_dog->age;
	our_dog->owner;

	return (our_dog);
}
